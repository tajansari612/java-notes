public class _02_printing_data {
    public static void main(String[] args) {
        //01 print data in single line
        System.out.print("Taj");
        System.out.print(" Mohammad");
        System.out.print(" Ansari");

        //02 println: to print data in multiple lines, prints every new data
        //in next line after println
        System.out.println();
        System.out.println("Taj");
        System.out.println("Mohammad");
        System.out.println("Ansari");

        //03 \n: \n is used in print statement to break the line into next line
        System.out.println("Taj\nMohammad\nAnsari");

        //Example
        System.out.println("*\n**\n***\n****");
    }
}
