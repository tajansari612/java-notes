
//OOPs
// Object-Oriented Programming (OOPs) is a programming paradigm that is based on the
// concept of objects. 
// These objects are created using classes, which are the focal point of OOPs. 
// The objects can interact with one another to design applications and computer 
// programs.
// OOPs concepts in Java are the main ideas behind Java's Object-Oriented 
// Programming. 
// They are an abstraction, encapsulation, inheritance, and polymorphism.

//1. Abstraction
// Abstraction is the concept of hiding the complex implementation details from the 
// user and only providing the necessary functionality. 
// It focuses on what the object does rather than how it does it.
// Example:
// Consider a car. The user only needs to know how to drive the car, not how the 
// engine works. This is an example of abstraction.


//2. Encapsulation
//Encapsulation is the concept of wrapping the data (variables) and code acting on 
// the data (methods) together as a single unit. It restricts direct access to some 
// of the object's components.
//Example:
//Consider a capsule. The medicine inside the capsule is encapsulated, and only 
// the outer layer is visible to the user. This is an example of encapsulation.


//3. Inheritance
//Inheritance is the concept of acquiring the properties and behavior of a parent 
// class by a child class. It promotes code reusability and is used to achieve 
// runtime polymorphism.
//Example:
//Consider a car. The car is a vehicle. Here, the vehicle is the parent class, 
// and the car is the child class. The car inherits the properties and behavior 
// of the vehicle class.


//4. Polymorphism
//Polymorphism is the concept of performing a single action in different ways. 
// It allows objects of different classes to be treated as objects of a common 
// superclass.
//Example:
//Consider a shape. A shape can be a circle, square, or rectangle. All these shapes 
// have an area, but the calculation of the area is different for each shape. 
// This is an example of polymorphism.

